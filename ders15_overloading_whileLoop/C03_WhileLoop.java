package ders15_overloading_whileLoop;

public class C03_WhileLoop {
    public static void main(String[] args) {

        // 2 basamaklı sayıları aralarında bir boşluk olacak şekilde yanyana yazdırın.

        for (int i = 10; i <= 99 ; i++) {

            System.out.print(i + " ");
        }

        System.out.println("");

        int i = 10;

        while (i<100){

            System.out.print(i + " ");
            i++;

        }



    }
}
